#!/usr/bin/env bash
# Создаёт по одному issue на каждую подтверждённую находку аудита (issue #11).
# Идемпотентность: пропускает создание, если issue с таким заголовком уже открыт.
set -euo pipefail

REPO="xlabtg/FinLayer"
REF="Подробности и контекст: [отчёт аудита](https://github.com/xlabtg/FinLayer/blob/issue-11-a37a9b6ca64e/docs/audit/2026-06-01-logic-audit.md) · родительский issue #11."

existing="$(gh issue list --repo "$REPO" --state all --limit 200 --json title --jq '.[].title')"

mk() {
  local title="$1"; shift
  local labels="$1"; shift
  local body="$1"; shift
  if printf '%s\n' "$existing" | grep -Fxq "$title"; then
    echo "SKIP (exists): $title"
    return 0
  fi
  local url
  url="$(gh issue create --repo "$REPO" --title "$title" --label "$labels" --body "$body"$'\n\n'"$REF")"
  echo "CREATED: $url"
}

